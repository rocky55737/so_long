char	*ft_strchr(const char *s, int c)
{
	int	ret;

	ret = 0;
	while (s[ret])
	{
		if (s[ret] == c)
			return ((char *)&s[ret]);
		ret++;
	}
	if (c == 0)
		return ((char *)&s[ret]);
	return (0);
}
